package com.eleven.adminMem.dto;

public class MemCareerDTO {

	private String start_date;
	private String end_date;
	private String car_name;
	private String mem_id;
	private String car_category;
	private String detail;
	private String car_idx;
	
	public String getStart_date() {
		return start_date;
	}
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	public String getEnd_date() {
		return end_date;
	}
	public void setEnd_date(String end_date) {
		this.end_date = end_date;
	}
	public String getCar_name() {
		return car_name;
	}
	public void setCar_name(String car_name) {
		this.car_name = car_name;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCar_category() {
		return car_category;
	}
	public void setCar_category(String car_category) {
		this.car_category = car_category;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getCar_idx() {
		return car_idx;
	}
	public void setCar_idx(String car_idx) {
		this.car_idx = car_idx;
	}
	
	
}
