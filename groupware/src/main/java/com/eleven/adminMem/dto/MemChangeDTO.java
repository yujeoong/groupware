package com.eleven.adminMem.dto;

public class MemChangeDTO {

	private String mem_id;
	private String pos_idx;
	private String duty_idx;
	private String modi_idx;
	private String dep_idx;
	private String date;
	private String comment; 
	 
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPos_idx() {
		return pos_idx;
	}
	public void setPos_idx(String pos_idx) {
		this.pos_idx = pos_idx;
	}
	public String getDuty_idx() {
		return duty_idx;
	}
	public void setDuty_idx(String duty_idx) {
		this.duty_idx = duty_idx;
	}
	public String getModi_idx() {
		return modi_idx;
	}
	public void setModi_idx(String modi_idx) {
		this.modi_idx = modi_idx;
	}
	public String getDep_idx() {
		return dep_idx;
	}
	public void setDep_idx(String dep_idx) {
		this.dep_idx = dep_idx;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	
	
}
