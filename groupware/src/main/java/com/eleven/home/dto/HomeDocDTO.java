package com.eleven.home.dto;

public class HomeDocDTO {

	private int no;
	private String doc_idx;
	private String subject;
	private String detail_state;
	private String mem_id; 
	private String order;
	private String app_date;
	private String doc_state;
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getDoc_idx() {
		return doc_idx;
	}
	public void setDoc_idx(String doc_idx) {
		this.doc_idx = doc_idx;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getDetail_state() {
		return detail_state;
	}
	public void setDetail_state(String detail_state) {
		this.detail_state = detail_state;
	}
	public String getDoc_state() {
		return doc_state;
	}
	public void setDoc_state(String doc_state) {
		this.doc_state = doc_state;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	} 
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getApp_date() {
		return app_date;
	}
	public void setApp_date(String app_date) {
		this.app_date = app_date;
	}
	
	
	
}
